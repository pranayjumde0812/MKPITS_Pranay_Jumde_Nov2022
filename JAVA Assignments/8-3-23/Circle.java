public class Circle
{
	public static void main(String[] args)
		{
			int r=6;
			double area,peri;
			peri=2*3.14*r;
			System.out.println("Perimeter of a circle : "+peri);

			area=3.14*r*r;
			System.out.println("Area of a circle : "+area);
		}
}

